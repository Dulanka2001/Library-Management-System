    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:    
        
        try {
                String a=bookIDTF.getText();
                int b;
                b = 0;
                String add1 = null;
                ResultSet r= new DBSearch().searchBook();//this method use to search the book table
        
            
            while(r.next()){
                
                add1=r.getString("isbn");                
               
                if(a.equals(add1)){
                   String add2=r.getString("bname");
                   bnTF.setText(add2);
                   String add3=r.getString("category");
                   bcTF.setText(add3);
                   b++;
                   break;
             
               }            
                              
            }
            if(b==0){
                JOptionPane.showMessageDialog(null,"INVALID BOOK ID","Error",JOptionPane.ERROR_MESSAGE);
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }     
     
      
    }//GEN-LAST:event_searchBtnActionPerformed

