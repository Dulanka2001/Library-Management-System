    private void issueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueBtnActionPerformed
        // TODO add your handling code here:
        try{
            String issuebookid=mIDTF.getText();
            String add;
            int count=0;
            String state="NOT AVAILABLE";
            ResultSet r= new Model.DBSearch().searchIssuebooktable();//this method use to srch for issue book table in DB
            
            while(r.next()){
                
                add=r.getString("bookid");             
               
                if(issuebookid.equals(add)){
                    JOptionPane.showMessageDialog(null,"This Book is already issued","Error",JOptionPane.ERROR_MESSAGE);
                    count++;
                }
                    }
            
            if(count==0){
                String issuedate=((JTextField)issueDateChooser.getDateEditor().getUiComponent()).getText();
                String duedate=((JTextField)dueDateChooser.getDateEditor().getUiComponent()).getText();

                IssueBookController.IssueBook(bookIDTF.getText(), bnTF.getText(), mIDTF.getText(), mNTF.getText(),issuedate,duedate);
                IssueBookController.UpdateBook(bookIDTF.getText(),state);
                clearField();
                
            }
        
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
        //new Home().setVisible(true);
    }//GEN-LAST:event_issueBtnActionPerformed

